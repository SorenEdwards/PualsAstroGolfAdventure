#[derive(Copy, Clone, Debug, PartialEq)]
pub enum TileType {
    Ground,
    Wall,
    Portal,
    Hole,
}

#[derive(Clone, Debug)]
pub struct Tile {
    loc: Point,
    sprite: Sprite,
    tile_type: TileType,
    reflect: bool,
    friction: f64,
}
impl Tile {
    pub fn new(x: usize, y: usize, tile_type: TileType) -> Self {
        let reflect: bool;
        let friction: f64;
        let asset_template: [u8; TILE_SIZE];

        let point = Point::new(x, y);

        match tile_type {
            TileType::Ground => {
                friction = 0.99980;
                reflect = false;
                asset_template = GROUND_TILE_ASSET;
            }
            TileType::Wall => {
                friction = 0.99980;
                reflect = true;
                asset_template = WALL_TILE_ASSET;
            }
            TileType::Portal => {
                friction = 0.99980;
                reflect = false;
                asset_template = PORTAL_TILE_ASSET;
            }
            TileType::Hole => {
                friction = 0.99980;
                reflect = false;
                asset_template = HOLE_TILE_ASSET;
            }
        };
        let bitmap = map_color_to_rgba(&asset_template);
        let sprite = Sprite {
            width: 16,
            height: 16,
            pixels: bitmap,
        };
        Tile {
            loc: point,
            sprite: sprite,
            tile_type: tile_type,
            friction: friction,
            reflect: reflect,
        }
    }
    fn draw(&self, frame: &mut [u8]) {
        blit(frame, &self.loc, &self.sprite);
    }
    pub fn get_type(&self) -> &TileType {
        &self.tile_type
    }
}

pub struct TileGrid {
    grid: Vec<Vec<Tile>>,
    xsize: usize,
    ysize: usize,
}

impl TileGrid {
    pub fn set_tile(&mut self, x: usize, y: usize, tile: Tile) {
        self.grid[y][x] = tile;
    }

    pub fn draw(&self, frame: &mut [u8]) {
        for row in self.grid.iter() {
            for tile in row.iter() {
                tile.draw(frame);
            }
        }
    }
    pub fn tile_at(&self, x: usize, y: usize) -> &Tile {
        let mut x_index: usize = x;
        let mut y_index: usize = y;
        if x >= self.xsize {
            x_index = self.xsize - 1;
        }
        if y >= self.ysize {
            y_index = self.ysize - 1;
        }

        &self.grid[y_index][x_index]
    }

    pub fn block_at(&self, x: usize, y: usize) -> &Tile {
        //Needed to integrate with the terminal numbering
        &self.grid[y + 1][x]
    }

    pub fn xsize(&self) -> usize {
        self.xsize
    }

    pub fn ysize(&self) -> usize {
        self.ysize
    }
    pub fn new() -> TileGrid {
        let grid = map1();
        let grid = TileGrid {
            grid: grid,
            xsize: 40,
            ysize: 23,
        };
        return grid;
    }
}

